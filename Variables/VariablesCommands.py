# Variables
# A variable is a box. And we put stuff inside and give it a name
# Assign a variable
box = 'pillows and stuff'

# We gave box a name and put stuff inside

print(box)
# This prints the stuff inside the box
# To re assign the box you do the same as setting it

box = 'Books'
print (box)

# You can also re assign it to a different data type
box = 14
